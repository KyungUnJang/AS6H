/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */
  ms_cnt++;
  if((ms_cnt % 10) == 0) 	  uTimerFlag    |= TIM_10ms;
  if((ms_cnt % 30) == 0) 	  uTimerFlag    |= TIM_30ms;
  if((ms_cnt % 100) == 0) 	uTimerFlag    |= TIM_100ms;
  if((ms_cnt % 200) == 0)	  uTimerFlag    |= TIM_200ms; 
  if((ms_cnt % 500) == 0) 	uTimerFlag    |= TIM_500ms;
  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */
  
	if (Local_Key.lK_ChatCnt)
		--Local_Key.lK_ChatCnt;
	if (RFR_Key.rK_ChatCnt)
		--RFR_Key.rK_ChatCnt;
	if (DMotorWaitime)
		--DMotorWaitime;
	if (SMotorWaitime)
		--SMotorWaitime;
  if (RFR_Key.rK_ChatCnt)
		--RFR_Key.rK_ChatCnt;
	
  /* USER CODE END TIM3_IRQn 1 */
}

